	<p class = "ptitle"><br>Project Purpose:</br></p>
	<p class = "paragraph">
	
	<br>
	To create a program that takes a user input of room numbers, class numbers, and days a class is
	to be scheduled, and output a class schedule for the rooms given. This project is also, to determine our
	understanding of the software development process and the different phases of development. This
	begins with planning and organizing our approach as well as consistently updating a GANTT chart to
	plan, manage, and control the time for different phases of the process. This is a base level of our project
	and provides the ability to set time expectations and deadlines, as well as account for various setbacks
	that can occur along the process.
	</br>
	
	<br>
	This project then goes into the development process itself, which involves designing a program
	at the very base level, this starts with how data flows in our program. To accomplish this, we use three
	levels of Data Flow Diagrams. The base level, or context, shows a high-level view of the data flow
	process, and how a user data moves through our program. The next level, level zero, shows a more
	detailed view of the process and allows a use to follow the data as it goes from the start of its flow, to
	the end output, and the various processes that occur in between. The level one diagram is an in-depth
	view of our scheduling process, how we manage data, what happens if an input meets a certain criteria,
	how does this class number, days to be scheduled, and room number actually turn into a scheduled
	class.
	</br>
	
	<br>
	This isn't our only process, software development occurs in different stages, and diagrams like
	entity-relationship diagrams and use-case diagrams show how users interact with our software. How
	does a client use our program? What happens when a client selects a certain button or uploads their
	information? How does this entity (user or client) engage with our software? These diagrams showcase
	this interaction and it is crucial to the design process to understand how a user can interact with our
	software. This project is a look into the software development process, from how users interact with our
	program, to the planning and time management aspects, testing, usersâ€™ interactions, and the actual user
	interface, this project is an entire view of how software goes from an idea from a brainstorming session,
	to a final product.
	</br>
